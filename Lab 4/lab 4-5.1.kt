@Composable
fun ComposeArticleApp() {
    // Replace these placeholders with actual resource IDs or use hardcoded strings
    ArticleCard(
        title = "Jetpack Compose Tutorial", // Placeholder text
        shortDescription = "An introduction to Jetpack Compose.", // Placeholder text
        longDescription = "Jetpack Compose simplifies and accelerates UI development on Android. Build beautiful and responsive UIs with less code and less hassle.",
        imagePainter = painterResource(id = android.R.drawable.ic_menu_gallery) // Placeholder drawable
    )
}

@Composable
private fun ArticleCard(
    title: String,
    shortDescription: String,
    longDescription: String,
    imagePainter: Painter,
    modifier: Modifier = Modifier,
) {
    Column(modifier = modifier) {
        Image(painter = imagePainter, contentDescription = null)
        Text(
            text = title,
            modifier = Modifier.padding(16.dp),
            fontSize = 24.sp
        )
        Text(
            text = shortDescription,
            modifier = Modifier.padding(start = 16.dp, end = 16.dp),
            textAlign = TextAlign.Justify
        )
        Text(
            text = longDescription,
            modifier = Modifier.padding(16.dp),
            textAlign = TextAlign.Justify
        )
    }
}

